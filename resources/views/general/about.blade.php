<h1>About the Chandra X-ray Center</h1>
<p>The CXC is based in Cambridge, Massachusetts, and is staffed by team members from the Smithsonian Astrophysical Observatory, the Massachusetts Institute of Technology, and Northrop Grumman (formerly TRW). Chandra and the CXC are managed by NASA-MSFC. The purpose of the CXC is to provide the support required by the science community to realize fully the potential of the Chandra X-ray Observatory (CXO). The Chandra X-ray Center changed its name from the Advanced X-ray Astrophysics Facility (AXAF) Science Center in December of 1998, as NASA renamed AXAF in honor of Subrahmanyan Chandrasekhar.<br><br>
